/* function de adoptar  */
function adoptar() {
let boton = document.querySelector(".boton-adoptar");
if(boton.innerText === "Adoptar") {
    boton.innerText = "Adoptado";
    boton.classList.add("adoptado")
} else {
    boton.innerText = "Adoptar"
    boton.classList.remove("adoptado")
}
}
